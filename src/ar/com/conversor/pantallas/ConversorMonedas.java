	private void btnConvertirActionPerformed(java.awt.event.ActionEvent evt) {
		double cantidadIngresada = (double) spnEdad.getValue();
		double monedaIngresada;
		double monedaRetornada;

		Monedas monedas = new Monedas();

		String listaDeMonedas[] = monedas.monedas;
		Map<String, Double> mapaDeMonedas = new HashMap();

		for (int i = 0; i < listaDeMonedas.length; i++) {
			mapaDeMonedas.put(listaDeMonedas[i], monedas.getDolar());

		}

		mapaDeMonedas.put("Dolar", monedas.getDolar());
		mapaDeMonedas.put("Euro", monedas.getEuro());
		mapaDeMonedas.put("Peso Argentino", monedas.getPesoArgentino());
		mapaDeMonedas.put("Peso Mexicano", monedas.getPesoMexicano());
		mapaDeMonedas.put("Real", monedas.getReal());

		monedaIngresada = mapaDeMonedas.get(cboMonedaIngresada.getSelectedItem());
		monedaRetornada = mapaDeMonedas.get(cboMonedaRetornada.getSelectedItem());

		double resultado = Math.round((cantidadIngresada / monedaIngresada * monedaRetornada) * 100d) / 100d;

		this.lblResultado.setText(
				"El resultado de la conversiÃ³n a " + cboMonedaRetornada.getSelectedItem() + " es: " + resultado);
	}

